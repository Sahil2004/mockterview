const VerifyCredentials = () => {
    const [tokenId, setTokenId] = useState("");
    const [credential, setCredential] = useState(null);
    const [loading, setLoading] = useState(false);
    const [error, setError] = useState("");

    const getInterviewCredential = async () => {
        if (!tokenId) {
            setError("Please enter a valid Token ID.");
            return;
        }

        try {
            setLoading(true);
            setError("");

            const provider = new ethers.JsonRpcProvider("http://127.0.0.1:8545");
            const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, provider);

            const report = await contract.getReport(tokenId);

            setCredential({
                name: report[0],
                date: report[1],
                feedback: report[2],
                rating: report[3],
            });
        } catch (err) {
            console.error(err);
            setError("Failed to fetch credentials. Make sure the Token ID is correct.");
        } finally {
            setLoading(false);
        }
    };

    return (
        <div className="flex flex-col items-center justify-center min-h-screen p-6 bg-gray-100">
            <div className="w-full max-w-lg p-6 bg-white rounded-lg shadow-md">
                <h2 className="text-2xl font-semibold text-center">Verify Interview Credentials</h2>

                <div className="flex gap-2 mt-4">
                    <input
                        type="text"
                        placeholder="Enter Token ID"
                        value={tokenId}
                        onChange={(e) => setTokenId(e.target.value)}
                        className="flex-grow p-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
                    />
                    <button
                        onClick={getInterviewCredential}
                        disabled={loading}
                        className="px-4 py-2 bg-blue-600 text-white rounded-md hover:bg-blue-700 disabled:opacity-50"
                    >
                        {loading ? "Fetching..." : "Verify"}
                    </button>
                </div>

                {error && <p className="text-red-500 mt-2">{error}</p>}

                {credential && (
                    <div className="mt-6 p-4 border rounded-lg bg-gray-50">
                        <h3 className="text-lg font-semibold">{credential.name}</h3>
                        <p className="text-sm text-gray-600">Date: {credential.date}</p>
                        <p className="mt-2">{credential.feedback}</p>
                        <span className="inline-block mt-2 px-3 py-1 bg-green-500 text-white rounded-md text-lg">
                            ⭐ {credential.rating} / 10
                        </span>
                    </div>
                )}
            </div>
        </div>
    );
};

export default VerifyCredentials;
